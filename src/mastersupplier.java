    private void table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id suplier");
        model.addColumn("suplier");
        model.addColumn("alamat suplier");
        model.addColumn("telepon suplier");
        model.addColumn("status");

        try{
            String sql = "SELECT * FROM suplier";
            java.sql.Connection conn = new config().akses();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet res = pst.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),
                    res.getString(2),res.getString(3),res.getString(4),
                    res.getString(5)
                });
            }
            table.setModel(model);
        }catch(SQLException ex){
            
        }
    }
    
