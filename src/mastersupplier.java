        txtId.setText(id_suplier);

        String suplier = table.getValueAt(baris, 1).toString();
        txtSuplier.setText(suplier);

        String alamat_suplier = table.getValueAt(baris, 2).toString();
        txtAlamat.setText(alamat_suplier);

        String telepon_suplier = table.getValueAt(baris, 3).toString();
        txtTelepon.setText(telepon_suplier);

        String status = table.getValueAt(baris, 4).toString();
        txtStatus.setText(status);
